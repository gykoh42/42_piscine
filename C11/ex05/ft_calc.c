#include "ft_h.h"

int	ft_plus(int num1, int num2)
{
	return (num1 + num2);
}

int	ft_minus(int num1, int num2)
{
	return (num1 - num2);
}

int	ft_multiple(int num1, int num2)
{
	return (num1 * num2);
}

int	ft_divide(int num1, int num2)
{
	return (num1 / num2);
}

int	ft_mod(int num1, int num2)
{
	return (num1 % num2);
}
