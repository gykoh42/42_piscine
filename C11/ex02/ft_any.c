int	ft_any(char **tab, int (*f)(char*))
{
	int	i;
	int	tmp;

	i = 0;
	while (tab[i] != '\0')
	{
		tmp = f(tab[i]);
		if (tmp != 0)
			return (1);
		i++;
	}
	return (0);
}
